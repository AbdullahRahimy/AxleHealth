Cypress.Commands.add('verifyTextVisibility', (selector, text1, text2) => {
    cy.get(selector).then($el => {
      const text = $el.text();
      expect(
        text === text1 || text === text2,
        `Expected text to be either "${text1}" or "${text2}", but got "${text}"`
      ).to.be.true;
    });
  });
  
